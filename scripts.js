function highlightText(text, filter) {
  const regex = new RegExp(`(${filter})`, "gi");
  return text.replace(regex, "<mark>$1</mark>");
}

function displayTopics(filter = "") {
  topicList.innerHTML = "";
  if (!filter.trim()) {
    topics.forEach((topic, i) => {
      const topicItem = document.createElement("li");
      topicItem.textContent = topic.title;
      topicItem.addEventListener("click", () => {
        if (activeTopicElement === topicItem) {
          collapseAllTopics();
          activeTopicElement = null;
        } else {
          collapseAllTopics();
          activeTopicElement = topicItem;
          expandSection(topics[i].subsections, topicItem);
        }
      });
      topicList.appendChild(topicItem);
    });
    return;
  }

  const lowerFilter = filter.toLowerCase();
  let foundAny = false;

  topics.forEach(topic => {
    const topicMatches = topic.title.toLowerCase().includes(lowerFilter);

    const matchedSubsections = topic.subsections
      .map(sub => {
        const subMatches =
          sub.title.toLowerCase().includes(lowerFilter) ||
          sub.content.toLowerCase().includes(lowerFilter);

        const matchedSubsubs = (sub.subsubsections || []).filter(subsub =>
          (subsub.title && subsub.title.toLowerCase().includes(lowerFilter)) ||
          (subsub.content && subsub.content.toLowerCase().includes(lowerFilter))
        );

        return subMatches || matchedSubsubs.length
          ? { ...sub, subsubsections: matchedSubsubs }
          : null;
      })
      .filter(Boolean);

    if (topicMatches || matchedSubsections.length) {
      foundAny = true;

      const topicItem = document.createElement("li");
      topicItem.classList.add("search-topic");
      topicItem.innerHTML = highlightText(topic.title, filter);

      const ul = document.createElement("ul");
      ul.classList.add("nested-list");

      matchedSubsections.forEach(sub => {
        const subItem = document.createElement("li");
        subItem.innerHTML = highlightText(sub.title, filter);
        subItem.addEventListener("click", e => {
          e.stopPropagation();
          articleTitle.textContent = sub.title;
          articleContent.innerHTML = marked.parse(sub.content);


        });

        if (sub.subsubsections?.length) {
          const subsubList = document.createElement("ul");
          subsubList.classList.add("nested-list");
          sub.subsubsections.forEach(subsub => {
            const subsubItem = document.createElement("li");
            subsubItem.innerHTML = highlightText(subsub.title, filter);
            subsubItem.addEventListener("click", e => {
              e.stopPropagation();
              articleTitle.textContent = subsub.title;
              articleContent.innerHTML = marked.parse(subsub.content);
            });
            subsubList.appendChild(subsubItem);
          });
          subItem.appendChild(subsubList);
        }

        ul.appendChild(subItem);
      });

      topicItem.appendChild(ul);
      topicList.appendChild(topicItem);
    }
  });

  if (!foundAny) {
    const li = document.createElement("li");
    li.textContent = "No topics found.";
    topicList.appendChild(li);
  }
}

function expandSection(sections, parentElement) {
  const existingList = parentElement.nextSibling;
  if (existingList && existingList.tagName === "UL") {
    existingList.remove();
  } else {
    const ul = document.createElement("ul");
    ul.classList.add("nested-list");

    sections.forEach(section => {
      const li = document.createElement("li");
      li.textContent = section.title;
      li.addEventListener("click", e => {
        e.stopPropagation();
        articleTitle.textContent = section.title;
        articleContent.innerHTML = marked.parse(section.content);
        if (section.subsubsections && section.subsubsections.length > 0) {
          expandSection(section.subsubsections, li);
        }
      });
      ul.appendChild(li);
    });

    parentElement.after(ul);
  }
}

function collapseAllTopics() {
  const allNested = document.querySelectorAll(".nested-list");
  allNested.forEach(ul => ul.remove());
}

